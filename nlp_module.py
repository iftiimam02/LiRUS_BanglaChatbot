def bangla_reply(text):
    replies = {
        "তুমি কে": "আমি লিরুস, আপনার সহকারী।",
        "তুমি কেমন আছো": "আমি ভালো আছি, আপনি কেমন আছেন?",
        "ধন্যবাদ": "আপনাকেও ধন্যবাদ!",
        "রিপোর্ট দিন": "এই নিন, রিপোর্ট।",
        "ওষুধ দিন": "নিন, আপনার ওষুধ।",
        "বিদায়": "বিদায়! আবার দেখা হবে।"
    }
    for phrase, reply in replies.items():
        if phrase in text:
            return reply
    return "দুঃখিত, আমি বুঝতে পারিনি। আবার বলুন।"
